int minDaysrequired(int M,int K,vector<int>bloomDay){
    if(M*K>bloomDay.size()){
        return -1;
    }
    vector<int> maxth;
    for(int i=0;i<bloomDay.size()-K;i+=K){ 
        if(bloomDay.size()-i-1 < K){
            continue;
        }
        int maxx = INT_MIN;
        for(int j=i;j<i+K;j++){
            maxx = max(maxx,bloomDay[j]);
        }
        maxth.push_back(maxx);
    }
    sort(maxth.begin(),maxth.end());

    return maxth[M-1];
}

